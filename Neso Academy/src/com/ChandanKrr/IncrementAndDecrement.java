package com.ChandanKrr;

public class IncrementAndDecrement {
    public static void main(String[] args) {
        int i = 1;
        int j = ++i;
        System.out.println("i is " + i + ", j is " + j);

        int a = 1;
        int b = a++;
        System.out.println("a is " + a + ", b is " + b);
    }
}
