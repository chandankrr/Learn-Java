package com.ChandanKrr;

public class StingConcatenation {
    public static void main(String[] args) {
        System.out.println("Chandan" + "Krr" + 9 + 1);
        System.out.println("Chandan" + "Krr" + (9 + 1));
        System.out.println("chandan".concat("kumar"));
        // System.out.println("10" * 91); // error
        System.out.println("chandan".concat("kumar").concat("91"));
    }
}
